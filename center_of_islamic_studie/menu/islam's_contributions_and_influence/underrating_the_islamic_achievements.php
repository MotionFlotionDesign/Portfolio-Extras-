<p>In his book, "History of the Intellectual Development of Europe," John William Draper stated,</p> 

<blockquote>"I have to deplore the systematic manner in which the literature of Europe has contrived to put out of sight our scientific obligations to the Mohammadans (misnomer for Muslims). 
