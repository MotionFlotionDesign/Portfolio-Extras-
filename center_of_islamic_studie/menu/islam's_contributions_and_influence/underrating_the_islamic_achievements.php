<blockquote>The Arab has left his intellectual impress on Europe, as before long Christendom will have to confess --- he has indelibly written it on the heavens, as any one may see who reads the names of the stars on a common celestial globe."</blockquote>

<p>It is not only deplorable to underrate the discoveries of the early Muslims, but it is far worse to ignore the mere existence of science as a whole to the Muslims. Here is what Robert Briffault in his book, "The Making of Humanity" states:</p>

<blockquote>"The debt of our science to that of the Arabs does not consist in startling discoveries or revolutionary theories, science owes a great deal more to Arab culture, it owes its existence. The Astronomy and Mathematics of the Greeks were a foreign importation never thoroughly acclimatized in Greek culture. The Greeks systematized, generalized and theorized, but the patient ways of investigation, the accumulation of positive knowledge, the minute method of science, detailed and prolonged observation and experimental inquiry were altogether alien to the Greek temperament.</p></blockquote> 

<blockquote>Only in Hellenistic Alexandria was any approach to scientific work conducted in the ancient classical world. What we call science arose in Europe as a result of new spirit of enquiry, of new methods of experiment, observation, measurement, of the development of mathematics, in a form unknown to the Greeks. That spirit and those methods were introduced into the European world by the Arabs.</blockquote> 

<blockquote>It is highly probable that but for the Arabs, modern European civilization would never have arisen at all; it is absolutely certain that but for them, it would not have assumed that character which has enabled it to transcend all previous phases of evolution."</blockquote>
