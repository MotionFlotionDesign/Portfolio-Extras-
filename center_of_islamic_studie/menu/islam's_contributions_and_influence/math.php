<p>The greatest scientific contribution Muslims made to the world is the creation of mathematical science. Algebra, Geometry, Algorithm and Arithmetic are at the heart of every scientific and social aspect of life.</p> 

<p>There is hardly a single device, business entity, industry, architecture built without the Arabic numerals, the decimal point, the sign and cosine, the ruler and the compass, all of which are Islamic inventions.</p> 

<p>Abulwafaa was the first person to demonistrate the sine theorom for spherical triangle: sin (a+b) = sin a cos b - cos a sin b. The word 'sine' is the exact translation of the arabic word jayb.</p>

<p>Muhammad Ibn Musa Al-Khowarizmi, the father of Algebra, was a mathematician and astronomer. He was summoned to Baghdad by Al-Mamun and appointed court astronomer. </p>

<p>The first book on Algebra was written by al Khawarzmi, Kitab al Mukhtasir fi Hisab al Jabr wa 'l-muqabalah' The book of Summary Concerning the Process of Calculating Compulsion and Equation.</p> 

<p>Al jaber is the restoration and amplification of something incomplete, and Muqabalah is the balancing of the two sides of an equation. Al-Khowarizmi emphasised that he wrote his Algebra book to serve the practical needs of the people concerning matters of inheritance, legacies, partition, lawsuits and commerce.</p> 

<p>In the 12th century, Gerard of Cremona and Roberts of Chester translated the Algebra of Al-Khowarizmi into Latin. Mathematicians used it all over the world until the 16th century. Mathematics as a science was found during the 10th century by mathematicians Al-Kharaji (d1000), Ibn al-Haytham, (d1040),and Umar al-Khayyam (d1130).</p>

<p>Ibn al-Haytham, who was a Physicist, Astronomer and Mathmatician used his math genius for the development of optics. In his book Kitab al-Manazir (The Book of Optics), he demonstrated the second law of refraction, or the incident ray. In a masterly faction he described the functions of the eye such as the connectives, iris, corona and lens. He also showed the interrelation between the various parts. </p>

<p>Ibn Al Haytham proved that the light enters the eye from an object or a "form" and he provided the mathematic models to prove it. He applied a geometrical method to the physical doctrine of "forms." He discussed whether the "form" large or small in color can enter through the pupil and make its way to the brain. His theory of vision is the correct theory of today's physiology.</p> 

<p>In the history of Mathematics, Ibn al Haytham secured a notable place by his treatment of the problem now bearing his Latinized name, Alhazen. He figured out the mathematical formula governing the entry and reflection of light at given three points, such as the eye and a point on a spherical convex mirror and the retina.</p> 

<p>Many of the intellectual sciences were developed as a direct result of Muslim needs to fulfill the ritual and duties of worship. The Islamic duty of Zakah or alms giving, and the distribution of properties in the will are yet other Islamic duties that laid the foundation of geometry and arithmetic. A Muslim is to give annually in charity and in taxation detailed amounts of currency and/or crops.</p> 

<p>Figuring out the exact distribution of Zakah and property does not come without complicated math. Each commodity requires a precise scale and percentage. For example, for an acre of an irregular piece of land that is to be split among a family of two boys and two girls, with the male share twice as that of the girl, a complicated formula and exact geometry must take place before this duty is accomplished. Thus, mathematics and geometry came to an invention.</p>

<p>The prominent historian, De Vaux, in his book, "The Philosophers of Islam" said:</p> 

<blockquote>"they (the Muslims) were indisputably the founders of plane and spherical geometry."</blockquote>
<p>He further stated:</p> 
