<p>Poetry was central to the social, political and spiritual Arab life. A poet was the representative and image-maker of his tribe, the media of broadcast, inflaming and arousing his people for war. He was the hummer and entertainer. Poetry was the media of communication and of history documentation. It was written with conformity and rhythmic patterns describing a seen or a heroic act of a knight, a trip or an animal and expressing passion and love stories.</p> 

<p>Arabic literature, biographical and historical books are surprisingly rich with poetry. The case for the Arabic poetry is no less in the Persian and Urdu literature. Persian (now known as Iran) mystic and poet, Rumi, verses are permeated by elements of Sufism, a movement of Islamic mysticism. He was born in Balkh, in what is now Afghanistan.
In 1247 Rumi’s friend and religious guide Shams al-Din, a Sufi dervish, disappeared unexplainably. Over the years Rumi composed nearly 30,000 verses expressing his feelings at this loss.</p>

<p>Rumi’s epic poem Spiritual Couplets (mid-13th century) had an enormous influence on Islamic literature and thought. His followers organized a Sufi sect in 1273 called Mawlawiyah, or Mevlevi, also known as the whirling dervishes.</p>

<p>Rumi’s work includes the following: Divan-e-Shams, a compendium of poetry in praise of Shams in over 45,000 verses written in Farsi (Persian), Mathnavi - Rumi’s most famous work in 7 books, and Fihi ma Fihi, introductory discourses on metaphysics. He also wrote over 24,660 couplets in Farsi and some Arabic. This work is also commonly referred to as the Persian Quoran.</p>

