<p>Systematic human anatomical dissection was no more a pursuit of medieval Islamic society than it was of medieval Christendom. It seems clear from the available evidence, however, that there were no explicit legal or religious strictures banning it. Indeed, many scholars in Islam lauded the study of anatomy, primarily as a way of demonstrating the design and wisdom of God, and there are some references in medical writings to dissection, though to what extent these reflect actual practice is problematic.</p> 

<p>Knowledge of human anatomy in medieval Islam was firmly based on the anatomical writings of Galen, who flourished in the 2nd century AD, and who to a large extent argued from analogy with animal structures. Galen's writings were available in the Islamic world through the translation of Hunayn ibn Ishaq and his collaborators.</p>

<p>There were two noteworthy contributions made to the history of anatomy and physiology by medieval Islamic writers: One was the result of chance observation: â€˜Abd al-Latif al-Baghdadi (d. 1231/629) was able to improve the description of the bones of the lower jaw and sacrum following the discovery of some skeletons during a famine in Egypt in 1200.</p> 

<p>The second was the description of the movement of blood through the pulmonary transit by the Syrian physician Ibn al-Nafis (d. 1288/678). Ibn al-Nafis composed a very popular epitome of the Canon of Medicine by Ibn Sina (Avicenna) which he called the Mujiz.</p> 

<p>In addition, however, Ibn al-Nafis also wrote a commentary on the Canon in which he criticized Ibn Sina for spreading his discussion of the anatomical over several different sections of the Canon. Ibn al-Nafis subsequently prepared a separate commentary on just the anatomical portions, and it was in this latter commentary that he explicitly stated that the blood in the right ventricle of the heart must reach the left ventricle by way of the lungs and not through a passage connecting the ventricles, as Galen had maintained.</p> 

<p>This formulation of the pulmonary circulation, sometimes called the 'lesser circulation', was made three centuries before Michael Servetus (d. 1553) and Realdo Colombo (d. 1559), the first Europeans to describe the pulmonary circulation.</p> 

<p>Because the discussions of anatomy are scattered throughout the huge Canon of Medicine by Avicenna (Ibn Sina), with the anatomy of a particular organ discussed only in the section concerned with diseases particular to that organ, these anatomical portions of the Canon were often copied out and compiled as a separate treatise. Two manuscripts in the NLM collection illustrate this interest in the anatomical portions of the Canon: (MS A 56, MS A 27, item 1). In both copies the margins contain extracts from the commentary written by Ibn al-Nafis on the anatomical portions of the Canon; the extracts are slightly more extensive in MS A 27 than in MS A 56.</p>

<p>All the major Arabic and Persian medical encyclopedias had sections on anatomy, summarizing the Galenic anatomical concepts. These were occasionally illustrated with schematic diagrams of the eye or the cranial sutures or the bones of the upper jaw or, occasionally the ventricles of the brain.</p> 

<p>Ar-Razi diagrammed many body parts, including the ventricles or cells of the brain illustrated in Kitab al-Mansuri fi al-tibb, cranial sutures, the upper jaw and teeth, and the visual system.</p>


<p>No anatomical illustrations of the entire body are known to have been produced in the Islamic world before those that usually accompany the Persian-language treatise Tashrih-i badan-i insan (The Anatomy of the Human Body) by Ibn Ilyas (circa 1390).</p>

<p><strong>What is Taught:</strong> William Harvey, during the early 17th century, discovered that blood circulates.</p>

<p>He was the first to correctly describe the function of the heart, arteries and veins.</p> 

<p>Rome's Galen had presented erroneous ideas regarding the circulatory system, and Harvey was the first to determine that blood is pumped throughout the body via the action of the heart and the venous valves. Therefore, he is regarded as the founder of human physiology.</p>

<p><strong>What Should be Taught:</strong> In the 10th century, Islam's ar-Razi wrote an in-depth treatise on the venous system, accurately describing the function of the veins and their valves.</p>

<p>Ibn an-Nafs and Ibn al-Quff (13th century) provided full documentation that the blood circulates and correctly described the physiology of the heart and the function of its valves 300 years before Harvey.</p>

<p>William Harvey was a graduate of Italy's famous Padua University at a time when the majority of its curriculum was based upon Ibn Sina's and ar-Razi's textbooks.</p>

<p>Ar-Razi devoted an entire book as a critique of Galen's anatomy.</p>

<p>The works of Paracelsus are insignificant compared to the vast volumes of medical writings and original findings accomplished by the medical giants of Islam.</p>

