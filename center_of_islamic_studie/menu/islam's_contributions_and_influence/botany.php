<div class="col-md-12">
<p>The Muslim scientists made considerable progress in biology and especially in botany. They developed horticulture to a high degree of perfection. They paid greater attention to botany in comparison to zoology. Botany reached its zenith in Spain.</p>

<p>Al-Masudi gave the rudiments of the theory of evolution in his well known work Meadows of Gold. Another of his works Kitab al-Tanbih wal Ishraq advances his views on evolution, namely from mineral to plant, from plant to animal and from animal to man. In botany Spanish Muslims made the greatest contribution and some of them are known as the greatest botanists of medieval times.</p>
<p>They were keen observers and discovered sexual difference between such plants as palms and hemps. They roamed about on seashores, mountains and in distant lands in search of rare botanical herbs. They classified plants into those that grow from seeds, cuttings and those that grow of their own accord, i.e., wild growth. The Spanish Muslims advanced in botany far beyond the state in which "it had been left by Dioscorides and augmented the herbology of the Greeks by the addition of 2,000 plants." Regular botanical gardens existed in Cordova, Baghdad, Cairo and Fez for teaching and experimental purposes.</p>

<p>Some of these gardens were the finest in the world. The Cordovan physician, Al-Ghafiqi (D. 1165) was a renowned botanist, who collected plants in Spain and Africa and described them most accurately. According to G. Sarton he was "the greatest expert of his time on simples. His description of plants was the most precise ever made in Islam; he gave the names of each in Arabic, Latin and Berber". His outstanding work Al Adwiyah al Mufradah, dealing with simples, was later appropriated by Ibn Baytar." Abu Zakariya Yahya Ibn Muhammad Ibn Al-Awwan, who flourished at the end of the 12th century in Seville (Spain), was the author of the most important Islamic treaties on agriculture during the medieval times entitled Kitab al Filahah.</p>

<p>The book treats more than 585 plants and deals with the cultivation of more than 50 fruit trees. It also discusses numerous diseases of plants and suggests their remedies. The book presents new observations on properties of soil and different types of manures. Abdullah Ibn Ahmad Ibn al-Baytar, was the greatest botanist and pharmacist of Spain; in fact the greatest of medieval times. He roamed about in search of plants and collected herbs on the Mediterranean littoral, from Spain to Syria, described more than 1,400 medical drugs and compared them with the records of more than 150 ancient and Arabian authors.</p> 

<p>The collection of simple drugs, composed by him, is the most outstanding botanical work in Arabic. "This book, in fact, is the most important for the whole period extending from Dioscorides down to the 16th century." It is an encyclopedic work on the subject. He later entered into the service of the Ayyubid king, al-Malik al-Lamil, as his chief herbalist in Cairo.</p>

<p>From there he traveled through Syria and Asia Minor and died in Damascus. One of his works, AI-Mughani-fi al Adwiyah al Mufradah,deals with medicine. The other, Al Jami Ji al Adwiyah al Mufradah, is a very valuable book containing simple remedies regarding animal, vegetable and mineral matters which have been described above. It deals also with 200 novel plants which were not known up to that time. Abdul Abbas Al-Nabati also wandered along the African Coast from Spain to Arabia in search of herbs and plants. He discovered some rare plants on the shore of the Red Sea.</p>

<p>Another botanist Ibn Sauri, was accompanied by an artist during his travels in Syria, who made sketches of the plants which they found. Ibn Wahshiya, wrote his celebrated work, al-Filahah al-Nabatiyah, containing valuable information about animals and plants.</p>

<p>Many cosmographical encyclopedias have been written by Arabs and Persians, which contain sections on animals, plants and stones, of which the best known is that of Zakariya al-Kaiwini, who died in 1283 A. D.</p>

<p>Al-Dinawari wrote an excellent “book of plants” and al-Bakri has written a book describing in detail the “Plants of Andalusia”. Ibn Maskwaih, a contemporary of Al-Beruni, advanced a definite theory about evolution. According to him plant life, at its lowest stage of evolution, does not need any seed for its birth and growth. Nor does it perpetuate its species by means of the seed. The great advancement of botanical science in Spain led to the development of agriculture and horticulture on a grand scale.</p>

<blockquote>"Horticulture improvements" says G. Sarton, "constituted the finest legacies of Islam and the gardens of Spain proclaim to this clay, one of the noblest virtues of her Muslim conquerors. The development of agriculture was one of the glories of Muslim Spain."</blockquote>
