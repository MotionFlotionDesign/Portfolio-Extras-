<p>In the Middle East during these centuries, the 'Abbasids, after their victory over the Umayyads, had transformed the Umayyads' Arab empire into a multinational Muslim empire. They moved the capital of the empire from Syria to Iraq, where they built a new capital, Baghdad, from which, during the next five centuries, they would influence many of the main events of Islamic history.</p>

<p>In the early period of Abbasid rule, al-Mansur, the second caliph of the dynasty, continued the reorganization of the administration of the empire along the lines that had been laid down by his Umayyad predecessor, Abd al-Malik. Much of the Abbasid administration, for example, was left in the hands of well-educated Persian civil servants, many of whom came from families that had traditionally served the Sassanid kings. The important office of wazir or vizier, chief counselor, may well have developed from Sassanid models.</p>

<p>The vizier was much more than an advisor; when the caliph was weak, a capable vizier became the most powerful man in the empire. The creation of the office of the vizier was only one of the innovations the Abbasids brought to statecraft.</p>

<p>Another was the development of the Umayyad postal system into an efficient intelligence service; postmasters in outlying provinces were the eyes and ears of the government and regular reports were filed with the central government on everything from the state of the harvest to the doings of dissident sects.</p> 

<p>Under the Abbasids too, a whole literature was created for the use and training of the clerical classes that had come into being. Since all government business was by now transacted in Arabic, manuals of correct usage were written for the instruction of non-Arabic speakers who had found government employment. There was also a vast literature on the correct deportment of princes, as well as anthologies of witty sayings and anecdotes with which to enliven one's epistolary style.</p>

<p>In some ways the Abbasids were more fortunate than the Umayyads. When, for example, al-Mansur died in 775 after a reign of twenty years, his son, al-Mahdi, inherited a full treasury and an empire that was more devoted to trade than war.
The developments in trade, indeed, are among the achievements of the Abbasids that are too often overlooked.</p>

<p>Because Islamic rule unified much of the Eastern world, thus abolishing many boundaries, trade was freer, safer, and more extensive than it had been since the time of Alexander the Great. Muslim traders, consequently, established trading posts as far away as India, the Philippines, Malaya, the East Indies, and China.</p>

<p>From the 8th to the 11th centuries, this trade was largely concerned with finding and importing basic necessities-grain, metals, and wood. To obtain them, of course, the Muslims had to export too, often using the imports from one region as exports to another: pearls from the Gulf, livestock from the Arabian Peninsula (particularly Arabian horses and camels), and one of the chief products, cloth. The Muslims also traded medicines, an offshoot of Abbasid advances in medical science, as well as paper and sugar.</p>

<p>This expansion of commercial activity led to other developments too. One was a system of banking and exchange so sophisticated that a letter of credit issued in Baghdad could be honored in Samarkand in Central Asia or Kairouan in North Africa.</p>

<p>The demands on trade also generated development of crafts. From Baghdad's large urban population, for example, came craftsmen of every conceivable sort: metalworkers, leatherworkers, bookbinders, papermakers, jewelers, weavers, druggists, bakers, and many more. As they grew in importance to the economy, these craftsmen eventually organized themselves into mutual-benefit societies which in some ways were similar to later Western guilds and which offered many social services: lodging travelers, engaging in pious works such as caring for orphans, and endowing schools. Because of this growth in commerce the Abbasids also developed a system by which a muhtasib, an inspector, made sure that proper weights and measures were given and that dishonest practices of all sorts were avoided.</p>
