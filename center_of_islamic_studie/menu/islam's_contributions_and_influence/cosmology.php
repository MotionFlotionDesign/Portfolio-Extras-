<p>By looking at the cosmos, the intelligence He put in us constantly meets His intelligence. Several Koranic verses draw the reader's attention to the numerical order that is present in the cosmos. The cosmic regularities which are a consequence of God's will, can thus be qualified as "mathematical regularities. </p>

<p>The esoteric doctrines of Islam go one step further. According to the views of the Akbarian school, funded after the work of Muhyi-d-din Ibn Arabi (1165 -- 1240), the Creation is God's self-disclosure of Himself through the veils and signs of the creatures.</p> 

<p>The things “are” not, since only God is. They only own a given preparation to receive being and qualities from God. The (relative) stability of cosmic phenomena is rooted in God's (absolute) immutability. However, the status of the cosmos is paradoxical, between absolute Being, God Himself, and absolute nothingness. As a consequence, we cannot expect to reach clear-cut statements about the fundamental reality of the world.</p> 

<p>According to the Islamic theology, God does not act by fixing the laws of physics and the initial conditions and letting the world evolve mechanistically. As a matter of fact, there is nothing like secondary causes, simply because God, as the “primary” cause, does not cease to create again the world at each instant. In this continuous renewal of creation, the atoms and their accidents are created anew at each time.</p> 

<p>The regularities that are observed in the world are not due to causal connection, but to a constant conjunction between the phenomena, which is a habit or custom established by God's will. This principle of the Islamic theology apparently contradicts the views of modern science, which of course stipulate the existence of secondary causes. But we must understand the negation of causality by the Islamic tradition as an emphasis on the metaphysical mystery of the continuous validity of the laws. This questioning on causality is not an obstacle to our scientific investigation of the cosmos. On the contrary, it prompts us to reflect upon the way God acts, and shows His signs “upon the horizons” (Koran, 41:53).</p> 

