<p>The Holy Quran had awakened a spirit of inquiry among the Arabs which was instrumental in their splendid achievements in the field of science and according to a western critic led them to realize that "science could not be advanced by mere speculation; its only sure progress lay in the practical interrogation of nature. </p>

<p>The essential characteristics of their method are experiment and observation.
The Muslims developed physics to a high degree and produced such eminent physicists as Kindi, al-Beruni, and Ar-Razi. Abu Yusuf Ibn Ishaq, known as al-Kindi was born at Kufa in the middle of the 9th century and flourished in Baghdad. He is the most dominating and one of the greatest Muslim scholars of physics. He applied mathematics not only to physics, but also to medicine. </p>

<p>He was therefore regarded by Cardon, a philosopher of the Renaissance, "as one of the 12 subtlest minds." He thought that gold and silver could only be obtained from mines and not through any other process. He endeavored to ascertain the laws that govern the fall of bodies.</p>

<p>Razi investigated on the determination of specific gravity of means of hydrostatic balance, called by him Mizan-al-Tabii. Most of his works on physics, mathematics, astronomy and optics have perished. In physics, his writings deal with matter, space, time and motion.</p> 

<p>In his opinion, matter in the primitive state before the creation of the world, was composed of scattered atoms, which possessed extent. Mixed in various proportions with the articles of void, these atoms produced these elements which are five in number, namely, earth, air, water, fire and celestial element.</p>

<p>Abu Rehan Beruni, was a versatile genius, who adorned the durbar of Mahmud of Ghazni. His outstanding achievement in the realm of physics was the accurate determination of the weight of 18 stones. He also discovered that light travels faster than sound. He has also contributed immensely to geological knowledge by providing the correct explanation of the formation of natural spring and artesian wells.</p> 

<p>He suggested that the Indus Valley was formerly an ancient basin filled with alluvial soil. His Kitab al Jawahir deals with different types of gems and their specific gravity. A voluminous unedited lapidary by Betuni is kept in manuscript form in the Escorial Library. It deals.with a large number of stones and metals from the natural, commercial and medical point of view.</p>

<p><strong>What is Taught:</strong> In the 17th century, the pendulum was developed by Galileo during his teenage years. He noticed a chandelier swaying as it was being blown by the wind. As a result, he went home and invented the pendulum.</p>

<p><strong>What Should be Taught:</strong> The pendulum was discovered by Ibn Yunus al-Masri during the 10th century, who was the first to study and document its oscillatory motion. Its value for use in clocks was introduced by Muslim physicists during the 15th century.</p>

<p><strong>What is Taught:</strong> Galileo (17th century) was the world's first great experimenter.</p>

<p><strong>What Should be Taught:</strong> Al-Biruni (d. 1050) was the world's first great experimenter. He wrote over 200 books, many of which discuss his precise experiments.</p>

<p>His literary output in the sciences amounts to some 13,000 pages, far exceeding that written by Galileo or, for that matter, Galileo and Newton combined.</p>

<p>Using math in Physics and Astronomy, Ibn Al-Haytham wrote treaties on the light of the moon, in which he argues that the moon shines like a self luminous object, though its light is borrowed from the sun. </p>

<p>He wrote on the Halo and Rainbow, on Spherical Burning Mirrors, on Paraboloidal Burning Mirrors, and on the shape of an eclipse, which examines the camera.</p>
