<p>Advising his followers to improve their standards of living and businesses, Prophet Muhammad said: "Poverty is as bad as disbelieving in God." This philosophy enticed Muslims to pursue and invent ways that would raise their standards of living. Free trade, competition in quality, urban development and the utilization of unused land were only a few of the pursuits that Muslims put into practice.</p>

<p>The Muslims were the pioneers of science and art during medieval times and formed the necessary link between the ancient and the modern. Their light of learning dispelled the gloom that had enveloped Europe. Moorish Spain was the main source from which the scientific knowledge of the Muslims and their great achievements were transmitted to France, Germany and England.</p>

<p>The Spanish universities of Cordoba, Seville and Granada were thronged with Christian and Jewish students who learned science from the Muslim scientists and who then popularized them in their native lands. </p>

<p>Another source for the transmission of Muslim scientific knowledge was Sicily, where during the reign of Muslim kings and even afterwards a large number of scientific works were translated from Arabic into Latin. </p>

<p>The most prominent translators who translated Muslims works from Arabic into European languages were Gerard of Cremona, Adelard of Bath, Roger Bacon and Robert Chester.</p>

<p>Writing in his celebrated work, Moors in Spain, Stanley Lane Poole says, "For nearly eight centuries under the Mohammadan rulers, Spain set out to all Europe a shining example of a civilized and enlightened State-arts, literature and science prospered as they prospered nowhere in Europe. Students flocked from France, Germany and England to drink from the fountain of learning which flowed down in the cities of Moors.</p> 

<p>The surgeons and doctors of Andalusia were in the vanguard of science; women were encouraged to serious study and the lady doctor was not always unknown among the people of Cordova.</p>

<p>Mathematics, astronomy and botany, history, philosophy and jurisprudence, were to be mastered in Spain, and Spain alone.</p> 

<p>The practical work of the field, the scientific methods of irrigation, the arts of fortification and ship building, of the highest and most elaborate products of the loom, the gravel and the hammer, the potter's wheel and mason's trowel, were brought to perfection by the Spanish Moors. Whatever makes a kingdom great and prosperous, whatever tends to refinement and civilization was found in Muslim Spain." The students flocked to Spanish cities from all parts of Europe to be infused with the light of learning which lit up Moorish Spain.</p>

<p>Another western historian writes, "The light of these universities shone far beyond the Muslim world, and drew students to them from east and west. At Cordoba, in particular, there were a number of Christian students and the influence of Arab philosophy coming by way of Spain upon universities of Paris, Oxford and North Italy and upon Western Europe thought generally, was very considerable indeed.</p> 

