<h3>The Islamic Calendar</h3>
<p>The Islamic calendar (or Hijri calendar) is a purely lunar calendar. It contains 12 months that are based on the motion of the moon, and because 12 synodic months are only 12 x 29.53=354.36 days, the Islamic calendar is consistently shorter than a tropical year, and therefore it shifts with respect to the Christian calendar. The calendar is based on the Qur'an (Sura IX, 36-37) and its proper observance is a sacred duty for Muslims. The Islamic calendar is the official calendar in countries around the Gulf, especially Saudi Arabia. But other Muslim countries use the Gregorian calendar for civil purposes and only turn to the Islamic calendar for religious purposes.</p> 

<p><strong>What is Taught:</strong>
Until the 14th century, the only type of clock available was the water clock. In 1335, a large mechanical clock was erected in Milan, Italy. This was possibly the first weight-driven clock. </p>

<p><strong>What Should be Taught:</strong>
 A variety of mechanical clocks were produced by Spanish Muslim engineers, both large and small, and this knowledge was transmitted to Europe through Latin translations of Islamic books on mechanics. These clocks were weight-driven.</p> 

<p>Designs and illustrations of epi-cyclic and segmental gears were provided. One such clock included a mercury escapement. The latter type was directly copied by Europeans during the 15th century. In addition, during the 9th century, Ibn Firnas of Islamic Spain, according to Will Durant, invented a watch-like device which kept accurate time.</p> 

<p>The Muslims also constructed a variety of highly accurate astronomical clocks for use in their observatories.</p>


<p><strong>What is Taught:</strong> 
 In the 17th century, the pendulum was developed by Galileo during his teenage years. He noticed a chandelier swaying as it was being blown by the wind. As a result, he went home and invented the pendulum.</p> 

<p><strong>What Should be Taught:</strong> 
The pendulum was discovered by Ibn Yunus al-Masri during the 10th century, who was the first to study and document its oscillatory motion.</p>

<p>Its value for use in clocks was introduced by Muslim physicists during the 15th century.</p>
