<h3>The Invention of the Camera</h3>
<p>Ibn-al Haytham and his student Kamal ad-Din teamed up making history when they first observed and record the camera obsecura phenomenon. The obsecura phenomenon is literary defined by Webster's dictionary as "dark chamber." Usually is a cardboard with a tiny pin hole, that began the ground work of a camera.</p>

