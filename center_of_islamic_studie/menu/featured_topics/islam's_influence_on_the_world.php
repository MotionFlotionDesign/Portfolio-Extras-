<h3>Introduction</h3>
<p>Perhaps the greatest gift Muslims gave to the nations of the world is Islam. It is Islam’s philosophy of utilizing the knowledge of the Qur’an, purity in the heart and clarity in the mind that was responsible for influencing the nations of the world to better serve God, raise the standards of living and promote justice and brotherhood for all.</p> 

<p>It is the principles outlining the mission of Prophet Muhammad that made a global change in the world’s scientific realm and the world’s social and spiritual culture that formed the Golden Age of Islam from the 7th-15th century.
God said in the Holy Qur'an:</p>

<blockquote>"A similar favor have you already received in that We have sent among you a Messenger (Muhammad) of your own, delivering to you Our Verses and purifying you and teaching you the Book (Qur'an) and the Wisdom.." <font size="2">Qur'an, 2:151</font></blockquote>

<p>It was the vehicle of Muslim achievements in combining reason and faith, accumulating useful knowledge and creating the experimental method that subjected Europeans to get out of their "Dark Ages" and into the renaissance. Regarding this fact, Robert Briffault in his book, "The Making of Humanity" states:</p>

<blockquote>"It was under the influence of the Arab and Moor revival of culture and not in the 15th century, that a real renaissance took place. Spain, not Italy, was the cradle of the rebirth of Europe..... Science was the most momentous contribution of Arab civilization to the modern world; but its fruits were slow in ripening. Not until long after Moorish culture had sunk back into darkness did the giant, which it had given birth to, rise in his might. It was not science only which brought Europe back to life. Other and manifold influence from the civilization of Islam communicated its first glow to European Life."</blockquote>

<p>It is clear to Muslims that reason and faith do not collide; instead they integrate and yield fertile goodness. Click on the tabulated links below to see some of the accomplishments early Muslims made for humanity in scientific and humanistic endeavors.</p>

