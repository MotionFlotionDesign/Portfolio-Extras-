<p>Allah, the Almighty said:<blockquote>"Do they not earnestly investigate, ponder, and study the Qur'an (for understanding of its guidance and benefits), or is it that their hearts are locked up (cannot see its illuminating light and guidance.)"<font size="2"> -Qur'an, 47:24</font></blockquote></p>

<p>The Sunnah of al-Tathabbut is the investigative and study approach to taking an action or resolving a problem. It follows a method which Prophet Muhammad (p) and his household exercised and brought about as a Sunnah, a way of life, for his followers. When A'isha, the Prophet's wife, was asked how the Prophet (p) had conducted his affairs, she responded:</p>

<blockquote>"His deeds and actions were continuous and stable."<font size="2"> -Muslim</font></blockquote>

<p>In a different narration, she said:</p>

<blockquote>When the household of the Prophet conducted an affair, (they conducted it with careful consideration, tathabbut).</blockquote>

<p>The Arabic word is a term used for a comprehensive evaluation and analysis of a matter before a judgment or a resolution is made of it. A judge, for example, will not give his verdict before he thoroughly studies, investigates, and examines the case. This process is called tathabbut. Furthermore, the Prophet (p) also said:</p>

<blockquote>Cautions and careful consideration is from Allah, and haste is from Satan.<font size="2"> -Muslim</font></blockquote>

<p>In another tradition (hadith), the Prophet (p) cautioned against doing something without consideration of its results or impact. See below the following hadith of the Prophet Muhammad (p).</p>

<blockquote>If you are going to do something, consider carefully its consequences. (if the consequence is good, then consider doing it, but if its consequence is bad, then do not do it. Modify it, find other alternatives and or conduct further studies that would yield successful results).<font size="2"> -Masnad Abi Ya'la</font></blockquote>

<p>The previous three sayings of wisdom of the Prophet (p), strongly point out that the sunnah of tathabbut is elemental for every true believer. Muslims must not underestimate the underlying value that this precious sunnah will play in the construction of the various moral, social, economic, and political building blocks of the Muslim Ummah.</p>
